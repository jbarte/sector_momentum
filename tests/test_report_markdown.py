import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent))
from src.report import build_report_markdown, write_report


def test_markdown_has_all_sections():
    md = build_report_markdown("2026-06-25", "RANKED_TBL", "MOVERS_SEC", "SWEDISH_SEC")
    assert "# Sector Momentum Report — 2026-06-25" in md
    assert "> Analytical tooling, not investment advice." in md
    assert "## Rankings\nRANKED_TBL" in md
    assert "## Movers\nMOVERS_SEC" in md
    assert "SWEDISH_SEC" in md
    assert "*Generated by Sector Momentum Scanner. Last scan: 2026-06-25.*" in md


def test_write_report_uses_same_markdown(tmp_path):
    path = write_report("2026-06-25", "R", "M", "S", output_dir=str(tmp_path))
    assert Path(path).read_text() == build_report_markdown("2026-06-25", "R", "M", "S")
